    private void cal_5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cal_5ActionPerformed
        ScriptEngineManager script = new ScriptEngineManager();
        ScriptEngine eng = script.getEngineByName("JavaScript");

        String opcio = evt.getActionCommand();
        //System.out.println(opcio);
        String temp = "";
        String total = "";
        switch (opcio) {
            case "CE": {
                cal_resultado.setText("");
            }
            break;
            case "*": {
                temp = cal_resultado.getText() + "*";
            }
            break;
            case "/": {
                temp = cal_resultado.getText() + "/";
            }
            break;
            case "-": {
                temp = cal_resultado.getText() + "-";
            }
            break;
            case "7": {
                temp = cal_resultado.getText() + "7";
            }
            break;
            case "8": {
                temp = cal_resultado.getText() + "8";
            }
            break;
            case "9": {
                temp = cal_resultado.getText() + "9";
            }
            break;
            case "+": {
                temp = cal_resultado.getText() + "+";
            }
            break;
            case "4": {
                temp = cal_resultado.getText() + "4";
            }
            break;
            case "5": {
                temp = cal_resultado.getText() + "5";
            }
            break;
            case "6": {
                temp = cal_resultado.getText() + "6";
            }
            break;
            case "1": {
                temp = cal_resultado.getText() + "1";
            }
            break;
            case "2": {
                temp = cal_resultado.getText() + "2";
            }
            break;
            case "3": {
                temp = cal_resultado.getText() + "3";
            }
            break;
            case "0": {
                temp = cal_resultado.getText() + "0";
            }
            break;
            case ".": {
                temp = cal_resultado.getText() + ".";
            }
            break;
            case "=": {
                temp = cal_resultado.getText();
                try {
                    total = eng.eval(temp).toString();
                    //System.out.println(total);
                } catch (ScriptException ex) {
                    System.out.println("Error");;
                }
            }
            break;
        }
        if (opcio.equals("=")) {
            cal_resultado.setText(total);
        } else {
            cal_resultado.setText(temp);
        }

    }//GEN-LAST:event_cal_5ActionPerformed

