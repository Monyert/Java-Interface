    private void form_boto_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_form_boto_generarActionPerformed
        //Generar un cuadre de text amb les dades marcades.
        try{
        radio_so_windows.setActionCommand("Windows");
        radio_so_linux.setActionCommand("Linux");
        radio_so_mac.setActionCommand("Mac");
        String sistema = grup_so.getSelection().getActionCommand();
        String valor1 = "", valor2 = "", valor3 = "";
        if (box_prog.isSelected()) {
            valor1 = "\n  Programació";
        }
        if (box_diss.isSelected()) {
            valor2 = "\n  Disseny";
        }
        if (box_adm.isSelected()) {
            valor3 = "\n  Administració";
        }
        String bola = Integer.toString(slider.getValue());

        JOptionPane.showMessageDialog(this,
                "Resultat de la Enquesta" + "\n"
                + "Sistema: " + sistema + "\n"
                + "Especialitat:"
                + valor1
                + valor2
                + valor3
                + "\nHores: " + bola, "Datos Introducidos", JOptionPane.PLAIN_MESSAGE);
        }catch(NullPointerException buit){
            JOptionPane.showMessageDialog(this,"Els camps estan buits!!. \n Torna a Intentar-ho.","Avis",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_form_boto_generarActionPerformed

